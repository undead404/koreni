export default function transliterateIntoPolish(text: string): string {
  if (!text) {
    return '';
  }

  return text.replaceAll(
    // eslint-disable-next-line regexp/no-obscure-range
    /[А-Яа-яЁёЄєЇїІіҐґ']/g,
    (char) => cyrillicToPolishMap[char] || char,
  );
}
